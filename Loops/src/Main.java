public class Main {
    public static void main(String[] args) {
        /*1.WHILE
        2.FOR
        3.DO WHILE
         */
        int i=0;
        //1
        while(i<=10){
            System.out.println(i);
            i++;
        }
        //2.
        for (i=0;i<=10;i++){
            System.out.println((i));
        }
        //3.
        do{
            System.out.println(i);
            i++;
        }while(i<=10);
    }
}